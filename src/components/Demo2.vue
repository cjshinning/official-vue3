<script>
import {reactive} from 'vue';
export default{
    setup(){
        const state = reactive({count: 0});

        function increment(){
            state.count++
        }

        return {
            state,
            increment
        }
    }
}
</script>

<template>
    <div>the state is: {{state.count}}</div>
    <button @click="increment">{{state.count}}</button>
</template>
